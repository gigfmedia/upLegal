-- Create documents storage bucket for lawyer verification
INSERT INTO storage.buckets (id, name, public) VALUES ('documents', 'documents', false);

-- Create policies for document uploads
CREATE POLICY "Lawyers can upload their own documents" 
ON storage.objects 
FOR INSERT 
WITH CHECK (bucket_id = 'documents' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Lawyers can view their own documents" 
ON storage.objects 
FOR SELECT 
USING (bucket_id = 'documents' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Lawyers can update their own documents" 
ON storage.objects 
FOR UPDATE 
USING (bucket_id = 'documents' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Lawyers can delete their own documents" 
ON storage.objects 
FOR DELETE 
USING (bucket_id = 'documents' AND auth.uid()::text = (storage.foldername(name))[1]);

-- Add verification documents field to profiles table
ALTER TABLE profiles ADD COLUMN verification_documents jsonb DEFAULT '[]'::jsonb;